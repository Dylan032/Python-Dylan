# Vraag de lengte van de rechthoekzijden van de driehoek op
triangle_sides = []

for i in range(3):
    side_length = float(input(f"Voer de lengte van rechthoekzijde {i + 1} in: "))
    triangle_sides.append(side_length)

print("Lengtes van de rechthoekzijden:", triangle_sides)

